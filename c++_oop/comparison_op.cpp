#include <iostream>
using namespace std;

int main()
{
    int a = 5, b = 4;

    cout << "Value of a==b is " << (a == b) << endl;
    cout << "Value of a!=b is " << (a != b) << endl;
    cout << "Value of a<b is " << (a < b) << endl;
    cout << "Value of a<=b is " << (a <= b) << endl;
    cout << "Value of a>b is " << (a > b) << endl;
    cout << "Value of a>=b is " << (a >= b) << endl;

    return 0;
}