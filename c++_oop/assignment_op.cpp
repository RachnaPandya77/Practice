#include <iostream>
using namespace std;

int main()
{
    // Assignment Operator
    // used to assign value to variable

    int a = 10;
    float b = 0.5;
    char c = 'r';

    cout << "Value of a is " << a << endl;
    cout << "Value of b is " << b << endl;
    cout << "Value of c is " << c << endl;

    return 0;
}